def Convert_Number_Into_ATCG(x):
    ATCG = 'A'
    if(x == 1):
        ATCG = 'T'
    if(x == 2):
        ATCG = 'C'
    if(x == 3):
        ATCG = 'G'
    return (ATCG)


def Convert_Into_Seq(x):
    s = ''
    for i in range(3):
        (m, n) = divmod(x, 4)
        s = Convert_Number_Into_ATCG(n) + s
        x = m
    return (s)


